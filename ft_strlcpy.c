#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	srcl;
	size_t	i;

	i = 0;
	while (src[i] != '\0')
		i++;
	srcl = i;
	if (size == 0)
		return (srcl);
	i = 0;
	while (src[i] != '\0' && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (srcl);
}
